package com.grayMatter;

public class Complex {
	
	int img;
	int real;
	public int getImg() {
		return img;
	}
	public void setImg(int img) {
		this.img = img;
	}
	public int getReal() {
		return real;
	}
	public void setReal(int real) {
		this.real = real;
	}
	public Complex(int img, int real) {
		super();
		this.img = img;
		this.real = real;
	}
	
	public Complex() {
		super();
	}
	
	

}
