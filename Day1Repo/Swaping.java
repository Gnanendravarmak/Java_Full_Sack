package com.grayMatter;

public class Swaping {
	
	public static void main(String[] args) {
		
		int a = 5;
		int b = 10;
		int c = 15;
		int d = 20;
		
		System.out.println("Swaping of 4 number: ");
		
		a = a+b+c+d;
		b = a-b-c-d;
		c = a-b-c-d;
		d = a-b-c-d;
		a = a-b-c-d;
		
		System.out.println(a+" "+b+" "+c+" "+d);
		
		int a1 = 5;
		int b1 = 10;
		int c1 = 15;
		int d1 = 20;
		int e1 = 25;
		
		
		System.out.println("Swaping of 5 number: ");
		
		a1 = a1+b1+c1+d1+e1;
		b1 = a1-b1-c1-d1-e1;
		c1 = a1-b1-c1-d1-e1;
		d1 = a1-b1-c1-d1-e1;
		e1= a1-b1-c1-d1-e1;
		a1 = a1-b1-c1-d1-e1;
		
		System.out.println(a1+" "+b1+" "+c1+" "+d1+" "+e1);
		
		
		
		
		
	}

}
